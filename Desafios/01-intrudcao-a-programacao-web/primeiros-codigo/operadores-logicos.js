/*-==============================================
OPERADORES DE LÓGICOS

&& "E" As duas condições devem ser verdadeiras para 
que a condição final seja verdadeira.

|| "OU" Uma das condições devem ser veradeiras para que a condiçõa final 
seja verdadeira.

================================================*/

console.log(6 == 6 && 6 == 6); //true
console.log(5 <= 4 || 5 >= 2); //true
console.log(5 === 4 && 4 != 4); //
console.log(!(6 > 5)); // Eu estou negando um expressão, tornando-a o inverso da opção obvia apresentada.

